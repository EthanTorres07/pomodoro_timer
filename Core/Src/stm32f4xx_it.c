/**
  * @brief  EXTI line detection callbacks.
  * @param  GPIO_Pin Specifies the pins connected EXTI line
  * @retval None
  */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    // Increment/decrement encoder counter with debounce
    if (GPIO_Pin == ENC_A_Pin)
    {
        static uint32_t last_it_time = 0;
        uint32_t current_time = HAL_GetTick();

        // Check if elapsed time is valid
        if (current_time - last_it_time >= G_DEBOUNCE_TIME_MS)
        {
            last_it_time = current_time;

            // Read ENC_B_Pin to determine direction
            // ENC_A != ENC_B -> CW
            // ENC_A == ENC_B -> CCW
            if (HAL_GPIO_ReadPin(ENC_B_GPIO_Port, ENC_B_Pin) !=
                    HAL_GPIO_ReadPin(ENC_A_GPIO_Port, ENC_A_Pin))
            {
                g_counter++;
            }
            else
            {
                g_counter--;
            }

        }
    }

    // Increase epoch time
    else if (GPIO_Pin == RTC_IN_Pin)
    {
        _epochTime++;
    }
    else if (GPIO_Pin == ENC_SW_Pin)
    {
        buttonPressed = 1;
    }
    else if (GPIO_Pin == BZR_EN_Pin)
    {
        uint32_t currentTime = HAL_GetTick();

        if (currentTime - lastInterruptTime > 50)
        {
            // Pull-up: LOW = ON, HIGH = OFF
            if (HAL_GPIO_ReadPin(PORTC, BZR_EN_Pin) == GPIO_PIN_RESET)
            {
                silentMode = 1; // Switch is Pressed / ON
            } else {
                silentMode = 0; // Switch is Released / OFF
            }
        }
    }

}

